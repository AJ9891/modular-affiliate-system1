import { access, mkdir, readFile, writeFile } from 'node:fs/promises'
import { constants } from 'node:fs'
import path from 'node:path'

const nftTarget = path.join(process.cwd(), '.next', 'server', 'middleware.js.nft.json')
const middlewareTarget = path.join(process.cwd(), '.next', 'server', 'middleware.js')
const middlewareManifestTarget = path.join(process.cwd(), '.next', 'server', 'middleware-manifest.json')
const middlewareMarker = '// generated by ensure-middleware-nft'

const ensureFile = async (target, content, label) => {
  try {
    await access(target, constants.F_OK)
    return false
  } catch {
    await mkdir(path.dirname(target), { recursive: true })
    await writeFile(target, content)
    console.log(`[build] created missing ${label}`)
    return true
  }
}

const createMiddlewareLoader = async () => {
  let middlewareFiles = []

  try {
    const manifestRaw = await readFile(middlewareManifestTarget, 'utf8')
    const manifest = JSON.parse(manifestRaw)
    const files = manifest?.middleware?.['/']?.files
    if (Array.isArray(files)) {
      middlewareFiles = files
    }
  } catch {
    // Fall back to pass-through stub when manifest is unavailable.
  }

  const requireLines = middlewareFiles
    .map((file) => (file.startsWith('server/') ? file.slice('server/'.length) : file))
    .map((file) => `require(${JSON.stringify(`./${file}`)})`)
    .join('\n')

  return `${middlewareMarker}
globalThis.self = globalThis.self || globalThis
${requireLines}

const entry = globalThis._ENTRIES?.middleware_middleware
const resolvedHandler =
  entry && (entry.middleware || entry.default || (typeof entry === 'function' ? entry : null))

async function middleware(...args) {
  if (typeof resolvedHandler === 'function') {
    return resolvedHandler(...args)
  }
  return new Response(null, { headers: { 'x-middleware-next': '1' } })
}

module.exports = middleware
module.exports.default = middleware
module.exports.middleware = middleware
`
}

const ensureCommonJsLoader = async () => {
  const middlewareLoader = await createMiddlewareLoader()

  try {
    const content = await readFile(middlewareTarget, 'utf8')
    const trimmed = content.trim()
    const isLegacyStub =
      trimmed === 'export {}' ||
      trimmed === 'module.exports = {}' ||
      (content.includes('x-middleware-next') && content.includes('module.exports.middleware = middleware'))

    if (content.includes(middlewareMarker) || isLegacyStub) {
      await writeFile(middlewareTarget, middlewareLoader)
      console.log('[build] updated middleware.js loader')
    }
  } catch {
    await mkdir(path.dirname(middlewareTarget), { recursive: true })
    await writeFile(middlewareTarget, middlewareLoader)
    console.log('[build] created missing middleware.js')
  }
}

await ensureFile(nftTarget, `${JSON.stringify({ version: 1, files: [] }, null, 2)}\n`, 'middleware.js.nft.json')
await ensureCommonJsLoader()
